module branch_unit (

    input  wire [31:0] rs1,
    input  wire [31:0] rs2,
    input  wire [2:0]  BranchType,

    output reg         take_branch

);

/////////////////////////////////////////////////////////////
// Branch Comparator
/////////////////////////////////////////////////////////////

always @(*) begin

    // Default
    take_branch = 1'b0;

    case (BranchType)

        //////////////////////////////////////////////////////
        // BEQ
        //////////////////////////////////////////////////////

        3'b000:
        begin
            take_branch = (rs1 == rs2);
        end

        //////////////////////////////////////////////////////
        // BNE
        //////////////////////////////////////////////////////

        3'b001:
        begin
            take_branch = (rs1 != rs2);
        end

        //////////////////////////////////////////////////////
        // BLT
        //////////////////////////////////////////////////////

        3'b100:
        begin
            take_branch = ($signed(rs1) < $signed(rs2));
        end

        //////////////////////////////////////////////////////
        // BGE
        //////////////////////////////////////////////////////

        3'b101:
        begin
            take_branch = ($signed(rs1) >= $signed(rs2));
        end

        //////////////////////////////////////////////////////
        // BLTU
        //////////////////////////////////////////////////////

        3'b110:
        begin
            take_branch = (rs1 < rs2);
        end

        //////////////////////////////////////////////////////
        // BGEU
        //////////////////////////////////////////////////////

        3'b111:
        begin
            take_branch = (rs1 >= rs2);
        end

        //////////////////////////////////////////////////////
        // Default
        //////////////////////////////////////////////////////

        default:
        begin
            take_branch = 1'b0;
        end

    endcase

end

endmodule