module alu (

    input  wire [31:0] A,
    input  wire [31:0] B,
    input  wire [3:0]  ALUControl,

    output reg  [31:0] Result,
    output wire        Zero

);

/////////////////////////////////////////////////////////////
// ALU Control Encoding
/////////////////////////////////////////////////////////////

localparam ALU_ADD   = 4'b0000;
localparam ALU_SLL   = 4'b0001;
localparam ALU_SLT   = 4'b0010;
localparam ALU_SLTU  = 4'b0011;
localparam ALU_XOR   = 4'b0100;
localparam ALU_SRL   = 4'b0101;
localparam ALU_OR    = 4'b0110;
localparam ALU_AND   = 4'b0111;
localparam ALU_SUB   = 4'b1000;
localparam ALU_SRA   = 4'b1001;

/////////////////////////////////////////////////////////////
// ALU Logic
/////////////////////////////////////////////////////////////

always @(*) begin

    //////////////////////////////////////////////////////////
    // Default
    //////////////////////////////////////////////////////////

    Result = 32'b0;

    //////////////////////////////////////////////////////////
    // ALU Operation Decode
    //////////////////////////////////////////////////////////

    case (ALUControl)

        //////////////////////////////////////////////////////
        // ADD
        //////////////////////////////////////////////////////

        ALU_ADD:
        begin
            Result = A + B;
        end

        //////////////////////////////////////////////////////
        // SLL
        //////////////////////////////////////////////////////

        ALU_SLL:
        begin
            Result = A << B[4:0];
        end

        //////////////////////////////////////////////////////
        // SLT (Signed)
        //////////////////////////////////////////////////////

        ALU_SLT:
        begin
            Result = ($signed(A) < $signed(B)) ? 32'd1 : 32'd0;
        end

        //////////////////////////////////////////////////////
        // SLTU (Unsigned)
        //////////////////////////////////////////////////////

        ALU_SLTU:
        begin
            Result = (A < B) ? 32'd1 : 32'd0;
        end

        //////////////////////////////////////////////////////
        // XOR
        //////////////////////////////////////////////////////

        ALU_XOR:
        begin
            Result = A ^ B;
        end

        //////////////////////////////////////////////////////
        // SRL
        //////////////////////////////////////////////////////

        ALU_SRL:
        begin
            Result = A >> B[4:0];
        end

        //////////////////////////////////////////////////////
        // OR
        //////////////////////////////////////////////////////

        ALU_OR:
        begin
            Result = A | B;
        end

        //////////////////////////////////////////////////////
        // AND
        //////////////////////////////////////////////////////

        ALU_AND:
        begin
            Result = A & B;
        end

        //////////////////////////////////////////////////////
        // SUB
        //////////////////////////////////////////////////////

        ALU_SUB:
        begin
            Result = A - B;
        end

        //////////////////////////////////////////////////////
        // SRA
        //////////////////////////////////////////////////////

        ALU_SRA:
        begin
            Result = $signed(A) >>> B[4:0];
        end

        //////////////////////////////////////////////////////
        // Default
        //////////////////////////////////////////////////////

        default:
        begin
            Result = 32'b0;
        end

    endcase

end

/////////////////////////////////////////////////////////////
// Zero Flag
/////////////////////////////////////////////////////////////

assign Zero = (Result == 32'b0);

endmodule