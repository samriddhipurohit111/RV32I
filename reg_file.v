module reg_file (

    input  wire        clk,
    input  wire        reset,
    input  wire        RegWrite,

    input  wire [4:0]  Rs1,
    input  wire [4:0]  Rs2,
    input  wire [4:0]  Rd,

    input  wire [31:0] WriteData,

    output wire [31:0] ReadData1,
    output wire [31:0] ReadData2

);

/////////////////////////////////////////////////////////////
// Register Array
/////////////////////////////////////////////////////////////

reg [31:0] RegisterFile [0:31];

integer i;

/////////////////////////////////////////////////////////////
// Sequential Write Logic
/////////////////////////////////////////////////////////////

always @(posedge clk) begin

    //////////////////////////////////////////////////////////
    // Reset
    //////////////////////////////////////////////////////////

    if (reset) begin

        for (i = 0; i < 32; i = i + 1)
            RegisterFile[i] <= 32'b0;

    end

    //////////////////////////////////////////////////////////
    // Register Write
    //////////////////////////////////////////////////////////

    else begin

        //////////////////////////////////////////////////////
        // x0 must ALWAYS remain zero
        //////////////////////////////////////////////////////

        if (RegWrite && (Rd != 5'd0))
            RegisterFile[Rd] <= WriteData;

    end

end

/////////////////////////////////////////////////////////////
// Combinational Read Ports
/////////////////////////////////////////////////////////////

assign ReadData1 =
            (Rs1 == 5'd0) ? 32'b0 :
            RegisterFile[Rs1];

assign ReadData2 =
            (Rs2 == 5'd0) ? 32'b0 :
            RegisterFile[Rs2];

endmodule