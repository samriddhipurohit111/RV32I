module alu_control (

    input  wire [1:0] ALUOp,
    input  wire [2:0] funct3,
    input  wire       funct7_5,

    output reg  [3:0] ALUControl

);

/////////////////////////////////////////////////////////////
// ALU Control Encoding
/////////////////////////////////////////////////////////////

localparam ALU_ADD   = 4'b0000;
localparam ALU_SLL   = 4'b0001;
localparam ALU_SLT   = 4'b0010;
localparam ALU_SLTU  = 4'b0011;
localparam ALU_XOR   = 4'b0100;
localparam ALU_SRL   = 4'b0101;
localparam ALU_OR    = 4'b0110;
localparam ALU_AND   = 4'b0111;
localparam ALU_SUB   = 4'b1000;
localparam ALU_SRA   = 4'b1001;

/////////////////////////////////////////////////////////////
// ALU Decode
/////////////////////////////////////////////////////////////

always @(*) begin

    //////////////////////////////////////////////////////////
    // Default
    //////////////////////////////////////////////////////////

    ALUControl = ALU_ADD;

    //////////////////////////////////////////////////////////
    // ALUOp Decode
    //////////////////////////////////////////////////////////

    case (ALUOp)

        //////////////////////////////////////////////////////
        // LOAD / STORE
        // address calculation = ADD
        //////////////////////////////////////////////////////

        2'b00:
        begin
            ALUControl = ALU_ADD;
        end

        //////////////////////////////////////////////////////
        // BRANCH
        // subtraction/comparison
        //////////////////////////////////////////////////////

        2'b01:
        begin
            ALUControl = ALU_SUB;
        end

        //////////////////////////////////////////////////////
        // R-Type / I-Type
        //////////////////////////////////////////////////////

        2'b10:
        begin

            case (funct3)

                //////////////////////////////////////////////
                // ADD / SUB
                //////////////////////////////////////////////

                3'b000:
                begin
                    if (funct7_5)
                        ALUControl = ALU_SUB;
                    else
                        ALUControl = ALU_ADD;
                end

                //////////////////////////////////////////////
                // SLL
                //////////////////////////////////////////////

                3'b001:
                begin
                    ALUControl = ALU_SLL;
                end

                //////////////////////////////////////////////
                // SLT
                //////////////////////////////////////////////

                3'b010:
                begin
                    ALUControl = ALU_SLT;
                end

                //////////////////////////////////////////////
                // SLTU
                //////////////////////////////////////////////

                3'b011:
                begin
                    ALUControl = ALU_SLTU;
                end

                //////////////////////////////////////////////
                // XOR
                //////////////////////////////////////////////

                3'b100:
                begin
                    ALUControl = ALU_XOR;
                end

                //////////////////////////////////////////////
                // SRL / SRA
                //////////////////////////////////////////////

                3'b101:
                begin
                    if (funct7_5)
                        ALUControl = ALU_SRA;
                    else
                        ALUControl = ALU_SRL;
                end

                //////////////////////////////////////////////
                // OR
                //////////////////////////////////////////////

                3'b110:
                begin
                    ALUControl = ALU_OR;
                end

                //////////////////////////////////////////////
                // AND
                //////////////////////////////////////////////

                3'b111:
                begin
                    ALUControl = ALU_AND;
                end

                //////////////////////////////////////////////
                // Default
                //////////////////////////////////////////////

                default:
                begin
                    ALUControl = ALU_ADD;
                end

            endcase

        end

        //////////////////////////////////////////////////////
        // Default
        //////////////////////////////////////////////////////

        default:
        begin
            ALUControl = ALU_ADD;
        end

    endcase

end

endmodule