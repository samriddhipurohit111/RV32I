module instr_mem_wrapper (

    input  wire        clk,

    input  wire        MemRead,

    input  wire [31:0] addr,

    output wire [31:0] instr_out

);

/////////////////////////////////////////////////////////////
// Internal Signals
/////////////////////////////////////////////////////////////

wire [8:0] rom_addr;

wire [15:0] q_low;
wire [15:0] q_high;

/////////////////////////////////////////////////////////////
// Word-Aligned Address
/////////////////////////////////////////////////////////////

assign rom_addr = addr[10:2];

/////////////////////////////////////////////////////////////
// ROM Macro Instantiation
/////////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////////
// LOWER 16 BITS
/////////////////////////////////////////////////////////////

rom_512x16A ROM_LOW (

    .CLK (clk),

    .CEN (~MemRead),

    .A   (rom_addr),

    .Q   (q_low)

);

/////////////////////////////////////////////////////////////
// UPPER 16 BITS
/////////////////////////////////////////////////////////////

rom_512x16A ROM_HIGH (

    .CLK (clk),

    .CEN (~MemRead),

    .A   (rom_addr),

    .Q   (q_high)

);

/////////////////////////////////////////////////////////////
// Combine 32-bit Instruction
/////////////////////////////////////////////////////////////

assign instr_out = {q_high, q_low};

endmodule